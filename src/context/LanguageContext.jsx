const LanguageContext = createContext({
  language: 'en',
  t: translations.en,
  setLanguage: () => {},
  availableLanguages: [],
});

export const LanguageProvider = ({ children }) => {
  const [language, setLanguageState] = useState('en');

  useEffect(() => {
    loadSavedLanguage();
  }, []);

  const loadSavedLanguage = async () => {
    try {
      const saved = await AsyncStorage.getItem('app_language');
      if (saved && translations[saved]) {
        setLanguageState(saved);
        console.log(`🌐 Language loaded: ${saved}`);
      }
    } catch (e) {
      console.log('Language load error:', e);
    }
  };

  const setLanguage = async code => {
    try {
      setLanguageState(code);
      await AsyncStorage.setItem('app_language', code);
      console.log(`🌐 Language set to: ${code}`);
    } catch (e) {
      console.log('Language save error:', e);
    }
  };

  const t = translations[language] || translations.en;

  const availableLanguages = Object.values(translations).map(lang => ({
    code: lang.code,
    name: lang.name,
    nativeName: lang.nativeName,
    flag: lang.flag,
  }));

  return (
    <LanguageContext.Provider
      value={{ language, t, setLanguage, availableLanguages }}
    >
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

export default LanguageContext;
